x1 = int(input("первое число"))
x2 = int(input("второе число"))
k = 0
if x1 < 1000000 and x2 < 1000000:
    while x1 < x2:
        u1 = x1 // 100000
        u2 = (x1//10000) % 10
        u3 = (x1//1000) % 10
        u4 = (x1//100) % 10
        u5 = (x1//10) % 10
        u6 = x1 % 10
        if u1 + u2 + u3 == u4 +u4 + u6:
            k += 1
        x1 += 1
    print(k)
else:
    print ("ошибка")